    text_value = re.sub(r"[^А-Яа-яA-Za-z0-9\s.,!?;:()%-]", " ", text_value)

    # Сокращаем повторяющиеся пробелы до одного пробела.
    text_value = re.sub(r"\s+", " ", text_value)

    # Убираем пробелы в начале и конце строки.
